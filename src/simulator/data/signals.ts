export function getSignalsBySourceType(sourceType: SignalDefinition['sourceType']) {
  return SIGNALS.filter((s) => s.sourceType === sourceType);
}

export function getSignalsByLever(lever: string) {
  return SIGNALS.filter((s) => s.behaviourLever === lever);
}

export function getSignalById(id: string) {
  return SIGNALS.find((s) => s.id === id);
}
